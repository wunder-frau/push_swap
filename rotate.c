#include "push_swap.h"

void	rotate(t_stack **stack)
{
	t_stack	*temp;
	t_stack	*tail;

	temp = *stack;
	*stack = (*stack)->next;
	tail = get_lstlast(*stack);
	temp->next = NULL;
	tail->next = temp;
}
